package java;

public class Students {
    //atributos
    private int id;
    private String name;
    private int age;
    private String origin;
    private int yearStudy;

    //constructor
    Students(int id,String name,int age,String origin,int yearStudy){
        this.id = id;
        this.name = name;
        this.age = age;
        this.origin = origin;
        this.yearStudy = yearStudy;
    }
}
